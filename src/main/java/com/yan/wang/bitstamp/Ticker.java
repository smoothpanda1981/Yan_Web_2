package com.yan.wang.bitstamp;

public class Ticker {

	public String high;
	public String last;
	public String timestamp;
	public String bid;
	public String vwap;
	public String volume;
	public String low;
	public String ask;
	public String open;
	public String percent_change_24;
	public String open_24;

	public String getHigh() {
		return high;
	}

	public void setHigh(String high) {
		this.high = high;
	}

	public String getLast() {
		return last;
	}

	public void setLast(String last) {
		this.last = last;
	}

	public String getTimestamp() {
		return timestamp;
	}

	public void setTimestamp(String timestamp) {
		this.timestamp = timestamp;
	}

	public String getBid() {
		return bid;
	}

	public void setBid(String bid) {
		this.bid = bid;
	}

	public String getVwap() {
		return vwap;
	}

	public void setVwap(String vwap) {
		this.vwap = vwap;
	}

	public String getVolume() {
		return volume;
	}

	public void setVolume(String volume) {
		this.volume = volume;
	}

	public String getLow() {
		return low;
	}

	public void setLow(String low) {
		this.low = low;
	}

	public String getAsk() {
		return ask;
	}

	public void setAsk(String ask) {
		this.ask = ask;
	}

	public String getOpen() {
		return open;
	}

	public void setOpen(String open) {
		this.open = open;
	}

	public String getPercent_change_24() {
		return percent_change_24;
	}

	public void setPercent_change_24(String percent_change_24) {
		this.percent_change_24 = percent_change_24;
	}

	public String getOpen_24() {
		return open_24;
	}

	public void setOpen_24(String open_24) {
		this.open_24 = open_24;
	}
}
