package com.yan.wang.bitstamp;

public class UserTransaction {
    private String usd;
    private String orderId;
    private String cryptoUsdName;
    private String cryptoUsd;
    private String datetime;
    private String fee;
    private String cryptoAmountName;
    private String cryptoAmount;
    private String btc;
    private String type;
    private String id;
    private String eur;

    public String getUsd() {
        return usd;
    }

    public void setUsd(String usd) {
        this.usd = usd;
    }

    public String getOrderId() {
        return orderId;
    }

    public void setOrderId(String orderId) {
        this.orderId = orderId;
    }

    public String getCryptoUsd() {
        return cryptoUsd;
    }

    public void setCryptoUsd(String cryptoUsd) {
        this.cryptoUsd = cryptoUsd;
    }

    public String getDatetime() {
        return datetime;
    }

    public void setDatetime(String datetime) {
        this.datetime = datetime;
    }

    public String getFee() {
        return fee;
    }

    public void setFee(String fee) {
        this.fee = fee;
    }

    public String getCryptoAmount() {
        return cryptoAmount;
    }

    public void setCryptoAmount(String cryptoAmount) {
        this.cryptoAmount = cryptoAmount;
    }

    public String getBtc() {
        return btc;
    }

    public void setBtc(String btc) {
        this.btc = btc;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getEur() {
        return eur;
    }

    public void setEur(String eur) {
        this.eur = eur;
    }

    public String getCryptoUsdName() {
        return cryptoUsdName;
    }

    public void setCryptoUsdName(String cryptoUsdName) {
        this.cryptoUsdName = cryptoUsdName;
    }

    public String getCryptoAmountName() {
        return cryptoAmountName;
    }

    public void setCryptoAmountName(String cryptoAmountName) {
        this.cryptoAmountName = cryptoAmountName;
    }
}
